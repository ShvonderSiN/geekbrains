"""
Задача 24: В фермерском хозяйстве в Карелии выращивают чернику.
Она растёт на круглой грядке, причём кусты высажены только по окружности.
Таким образом, у каждого куста есть ровно два соседних. Всего на грядке растёт N кустов.
Эти кусты обладают разной урожайностью, поэтому ко времени сбора на них выросло различное число ягод — на i-ом кусте выросло ai ягод.
В этом фермерском хозяйстве внедрена система автоматического сбора черники.
Эта система состоит из управляющего модуля и нескольких собирающих модулей. Собирающий модуль за один заход,
находясь непосредственно перед некоторым кустом, собирает ягоды с этого куста и с двух соседних с ним.
Напишите программу для нахождения максимального числа ягод, которое может собрать за один заход собирающий модуль,
 находясь перед некоторым кустом заданной во входном файле грядки.
"""

n = int(input("Введите количество кустов черники: "))

berries = []
for _ in range(n):
    num_berries = int(input("Введите количество ягод на кусте: "))
    berries.append(num_berries)

max_berries = 0

for i in range(n):
    # Индексы соседних кустов
    left_index = (i - 1) % n
    right_index = (i + 1) % n

    # Сумма ягод текущего куста и его двух соседних кустов
    total_berries = berries[i] + berries[left_index] + berries[right_index]

    # Обновление максимального количества ягод
    max_berries = max(max_berries, total_berries)

print("Максимальное количество ягод, которое может собрать собирающий модуль:", max_berries)
